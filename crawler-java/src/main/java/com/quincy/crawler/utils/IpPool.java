    public static  ArrayList<Ip> ipArrayList = new ArrayList<>(10);

    public static boolean ipIsChecked(){
        return IpPool.ipArrayList.size() == 0;
    }

    public static boolean ipIsAvailable(Ip ip){
        Connection proxy = Jsoup.connect("https://book.douban.com/subject/1148282/").proxy(ip.getIp(),ip.getPort());
        Document document = null;
        try {
            document = proxy.userAgent(getUserAgent()).timeout(5000).post();
        } catch (IOException e) {
            logger.error("ip不可用[{}]",ip.toString());
        }
        return document == null;
    }

    private static Ip getIp(){
        if (ipIsChecked()) {
            HttpClientUtils.getIp();
        }
        return IpPool.ipArrayList.remove(0);
    }

    public static Ip  getAvailableIp(){
        Ip ip = getIp();
        while (ipIsAvailable(ip)) {
            ip = getIp();
        }
        return ip;
    }


